    private void cmdEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEditActionPerformed
int selectedRow = table.getSelectedRow();
if (selectedRow != -1) {
    String selectedId = table.getValueAt(selectedRow, 0).toString(); // ambil ID string
    String nama = table.getValueAt(selectedRow, 1).toString();

    CreateBarang.setEditMode(true);
    CreateBarang.setSelectedId(selectedId);  // ubah tipe parameter jadi String di CreateBarang juga
    CreateBarang.setName(nama);

    CreateBarang.setVisible(true);
} else {
    JOptionPane.showMessageDialog(this, "Pilih baris untuk diedit!");
}

    }//GEN-LAST:event_cmdEditActionPerformed

