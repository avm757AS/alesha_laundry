void loadTable() {
        DefaultTableModel tbl = new DefaultTableModel() {
            @Override
            public Class<?> getColumnClass(int column) {
                if (column == 0) return Boolean.class; // Checkbox
                return super.getColumnClass(column);
            }

            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 0; // Checkbox only editable
            }
        };

        tbl.addColumn("Select");
        tbl.addColumn("#");
        tbl.addColumn("ID");
        tbl.addColumn("Nama");
        tbl.addColumn("Stok");

        try {
            String query = "SELECT * FROM barang";
            ResultSet rs = DatabaseConnection.getData(query);
            int no = 1;
            while (rs.next()) {
                String id = rs.getString("id");
                String nama = rs.getString("nama");
                String stok = rs.getString("stok");
                tbl.addRow(new Object[]{false, no++, id, nama, stok});
                
                table.setModel(tbl);
                table.getColumnModel().getColumn(0).setPreferredWidth(30);  // Checkbox
                table.getColumnModel().getColumn(1).setPreferredWidth(30);  // #
                table.getColumnModel().getColumn(2).setPreferredWidth(200); // ID
                table.getColumnModel().getColumn(3).setPreferredWidth(250); // Nama
                table.getColumnModel().getColumn(4).setPreferredWidth(200);  // Stok
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }

        table.setModel(tbl);
        sorter = new TableRowSorter<>(tbl);
        table.setRowSorter(sorter);

        // Checkbox di header
        table.getColumnModel().getColumn(0).setHeaderRenderer(new CheckBoxTableHeaderRenderer(table, 0));
    
    }

private void initSearch() {
        txtSearch.getDocument().addDocumentListener(new DocumentListener() {
            public void insertUpdate(DocumentEvent e) { search(); }
            public void removeUpdate(DocumentEvent e) { search(); }
            public void changedUpdate(DocumentEvent e) { search(); }

            private void search() {
                String keyword = txtSearch.getText().trim();
                if (keyword.isEmpty()) {
                    sorter.setRowFilter(null);
                } else {
                    sorter.setRowFilter(RowFilter.regexFilter("(?i)" + keyword));
                }
            }
        });
    }
    
